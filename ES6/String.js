// includes
console.log('abc'.includes(b)) // true

// startsWith
console.log('abc'.startsWith('a')) // true

// endsWith
console.log('abc'.endsWith('c')) // true

// repeat
console.log('abc'.repeat(3)) // 'abcabcabc'

// padStart 从开头填充直到达到长度
console.log('abc'.padStart(10, '0')) // '0000000abc'

// padEnd
console.log('abc'.padEnd(10, '0')) // 'abc0000000'